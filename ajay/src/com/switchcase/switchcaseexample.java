package com.switchcase;

public class switchcaseexample {

	public static void main(String[] args) {
    String month="march";
    switch(month)
    {
     case "january":
    	 System.out.println("1");
    	 break;
     case "february":
    	 System.out.println("2");
    	 break;
     case "march":
    	 System.out.println("3");
    	 break;
    	 
    

	}

}
}